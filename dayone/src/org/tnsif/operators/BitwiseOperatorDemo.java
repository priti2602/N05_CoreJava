//Program to demonstrate on Bitwise operator

package org.tnsif.operators;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
		int x=15,y=3;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		System.out.println(x>>y);
		System.out.println(x<<y);
		

	}

}
